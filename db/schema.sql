DROP DATABASE IF EXISTS companyDB;
CREATE DATABASE companyDB;

use companyDB;

CREATE TABLE departments (
    id INT AUTO_INCREMENT  NOT NULL,
    department_name 
)

CREATE TABLE roles (
    id INT AUTO_INCREMENT NOT NULL,
    role_title  

)

CREATE employees (
    id INT NOT NULL, 
    first_name VARCHAR(30),
    last_name VARCHAR(30),

)